    /**
     * ****************************---- LOGIN * ----
     *
     *****************************
     * @param user
     * @param pass
     */
    public void checkLogin(String user, String pass) {
        try {
            String sql = "SELECT * FROM staff WHERE TENDN=? AND MATKHAU=?";
            PreparedStatement ps;
            ResultSet rs;
            ps = mySqlConnectt().prepareStatement(sql);
            ps.setString(1, user);
            ps.setString(2, pass);
            rs = ps.executeQuery();
            if (rs.next()) {
                String tdn = rs.getString("TENDN");
                String mk = rs.getString("MATKHAU");
                if (tdn.equals(user) && mk.equals(pass)) {
                    String type = rs.getString("TYPE");
                    jTFTenDN.setText(rs.getString("TENNV"));
                    if (type.equals("Admin")) {
                        JOptionPane.showMessageDialog(null, " Welcome Administrator ");
                        this.setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(null, " Welcome Staff ");
                        this.setVisible(true);
                        jBStaff.setEnabled(false);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, " Username or Password Incorrect");
                    LoginJF login = new LoginJF();
                    login.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, " Username or Password Incorrect");
                LoginJF login = new LoginJF();
                login.setVisible(true);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(BookSql.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

